fn main() {
    let x = 42;
    let y: u32 = 100;
    let z: f64 = 3.14;
    let is_rust_fun: bool = true;
    let ch: char = 'A';
    let s: String = String::from("Hello, world!");

    println!("x = {}", x);
    println!("y = {}", y);
    println!("z = {}", z);
    println!("is_rust_fun = {}", is_rust_fun);
    println!("ch = {}", ch);
    println!("s = {}", s)
}
